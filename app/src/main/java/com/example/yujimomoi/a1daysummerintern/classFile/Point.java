public class Point {
	public float x;
	public float y;

	public Point() {
		this.x = 0;
		this.y = 0;
	}

	Point(float x, float y) {
		this.x = x;
		this.y = y;
	}

	public void SetPoint(float x,float y) {
		this.x = x;
		this.y = y;
	}
}